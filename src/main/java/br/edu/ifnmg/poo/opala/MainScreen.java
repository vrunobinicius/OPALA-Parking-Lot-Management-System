    public void FillTable(JTable table, String Query) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        VehicleDAO vDao = new VehicleDAO();
        
        List<Vehicle> vList = vDao.findAll();
        
        for (Vehicle v : vList) {
            model.addRow(new Object[] {
                v.getId(),
                v.getLicensePlate()
            });
        }
    }

